<p class="regular" id="mainText" style="display:none;">
Liubishuhuland is a modern-day socio-politico-economic entity founded in 2010. As it exhibits characteristics of historical Utopian communities, Liubishuhuland is often referred to as one. The mission of Liubishuhuland is to improve the lives of its members through its unique culture and society and a specially designed Cooperative Commonwealth government-economy.
<br/><br/>
Geographically, Liubishuhuland is a collection of scattered sites overseen by one central location. The locations of Liubishuhuland are only disclosed to prospective members who advance far enough in the application process.
<br/><br/>
Application for Membership is a difficult process designed to wear off the resolve of all candidates except for the most dedicated. The application process is organized into 4 phases, each described below:
<br/><br/><b>Phase 1</b><br/>
Each applicant will be provided the Liubishuhu Language Guide, the Liubishuhu-English Dictionary, and the Application for Membership. Once the Application for Membership is completed and mailed or submitted digitally to the Admissions Office, reviewers will inform the applicant whether the application can be continued.
<br/><br/><b>Phase 2</b><br/>
Each applicant who passes Phase 1 will be provided an identification code and an written examination over Liubishuhuland, the Liubishuhu language, mathematics, English, and general knowledge. Upon its completion and submission to the Admissions Office, the examination will be scored by reviewers, who will inform the applicant whether the application can be continued.
<br/><br/><b>Phase 3</b><br/>
Each applicant who passes Phase 2 will be invited to a physical or online interview with a member. After a successful interview, the applicant will be provided a list of locations where he/she can enter into a temporary residence.
<br/><br/><b>Phase 4</b><br/>
The applicant will enter into residence, during which he/she will be observed by the community until deemed worthy of full membership.
